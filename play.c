#include "power.h"

void	play(char **map, int j, char c)
{
	int i;

	i = height_of_array(map) - 1;
	while (i >= 0)
	{
		if (map[i][j] == '.')
		{
			map[i][j] = c;
			return ;
		}
		i--;
	}
}
