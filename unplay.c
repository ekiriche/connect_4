#include "power.h"

int		unplay(char **map, int j)
{
	int i;

	i = 0;
	while (i < height_of_array(map))
	{
		if (map[i][j] != '.')
		{
			map[i][j] = '.';
			return (1);
		}
		i++;
	}
	return (0);
}
